# Extract num_bits starting at bit_start from byte_offset (little-endian)
def extract_bits(data, byte_offset, bit_start, num_bits):
    if byte_offset >= len(data):
        return 0
    # Read enough bytes to cover the bit range
    bytes_needed = (bit_start + num_bits + 7) // 8
    value = 0
    for i in range(bytes_needed):
        if byte_offset + i < len(data):
            value |= data[byte_offset + i] << (8 * i)
    # Extract the bits
    return (value >> bit_start) & ((1 << num_bits) - 1)

# Swap bytes for endianness conversion (2 or 4 bytes)
def convert_endianess(data, index, length):
    if length == 2:
        data[index], data[index+1] = data[index+1], data[index]
    elif length == 4:
        data[index], data[index+1], data[index+2], data[index+3] = \
        data[index+3], data[index+2], data[index+1], data[index]

