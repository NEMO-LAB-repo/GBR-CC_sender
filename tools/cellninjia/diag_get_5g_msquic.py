    # Convert TTI to Unix timestamp using baseline + TTI difference
    def _calculate_cellular_timestamp(self, current_tti):
        current_unix_time = time.time()
        
        if self._baseline_unix_timestamp is None:
            # First time - set baseline
            self._baseline_unix_timestamp = current_unix_time
            self._baseline_tti = current_tti
            return current_unix_time
        
        # Calculate TTI difference with proper wraparound handling
        # TTI wraps around at 10240 (0-10239)
        baseline_tti_total = self._baseline_tti + 10240 * self._tti_loop_count
        current_tti_total = current_tti + 10240 * self._tti_loop_count
        
        # Check if we need to increment loop count (TTI wrapped around)
        # This happens when current_tti < previous_tti significantly
        if not hasattr(self, '_previous_tti'):
            self._previous_tti = current_tti
        else:
            # Detect wraparound: if current TTI is much smaller than previous TTI
            if current_tti < self._previous_tti and (self._previous_tti - current_tti) > 5000:
                self._tti_loop_count += 1
                current_tti_total = current_tti + 10240 * self._tti_loop_count
        
        self._previous_tti = current_tti
        
        # Calculate time difference based on TTI difference
        tti_diff = current_tti_total - baseline_tti_total
        time_diff_seconds = tti_diff / 1000.0  # Each TTI = 1ms
        
        # Calculate cellular timestamp
        cellular_timestamp = self._baseline_unix_timestamp + time_diff_seconds
        
        return cellular_timestamp

