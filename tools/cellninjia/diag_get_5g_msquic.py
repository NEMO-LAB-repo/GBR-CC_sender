                #     "readable_timestamp": readable_timestamp, "version": version, 
                #     "subfn": subfn, "sysfn": sysfn, 
                #     "mcs_index": mcs_index, "tbs_string": get_tbs_index_string(tbs_index),
                #     "num_of_resource_blocks": num_of_resource_blocks, "start_of_resource_block": -1, 
                #     "riv_width": -1, "riv_value": -1,
                #     "is_ul_grant": 1
                # }
                record_data = {
                    "logcode": logcode, "timestamp": timestamp_new,
                    "mcs_index": mcs_index, "redundancy_version": redundancy_version,
                    "tbs_index": tbs_index, "num_of_resource_blocks": num_of_resource_blocks
                }
                tti_value = sysfn * 10 + subfn

