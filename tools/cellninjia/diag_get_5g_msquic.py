                convert_endianess(ul_grant_data, 6, 2)
                
                tbs_index = (ul_grant_data[2] & 0xFC) >> 2
                mcs_index = ((ul_grant_data[2] & 0x03) << 3) | ((ul_grant_data[3] & 0xE0) >> 5)

                redundancy_version = (ul_grant_data[3] & 0x18) >> 3
                
                # record_data = {
