    # Calculate ratio = allocated_resource / requested_resource using BSR and PUSCH data
    def calculate_ratio(self, bsr_tti, bsr_record):
        buffer_size = bsr_record["buffer_size"]
        lcg0_bsr_index = buffer_size[0]  # LCG 0 is the first element (index 0)

        # Handle invalid LCG 0 index (treat as 0)
        # if lcg3_bsr_index < 0:
        #     lcg3_bsr_index = 0

        # 5G NR BSR Table (TS 38.321): index -> buffer size in bytes
        lower_bounds = [
            0, 10, 14, 20, 28, 38, 53, 74,          # 0-7
            102, 142, 198, 276, 384, 535, 745, 1038, # 8-15
            1446, 2014, 2806, 3909, 5446, 7587, 10570, 14726,  # 16-23
            20516, 28581, 39818, 55474, 77284, 107669, 150000, 150000,  # 24-31
            150000, 150000, 150000, 150000, 150000, 150000, 150000, 150000,  # 32-39
            150000, 150000, 150000, 150000, 150000, 150000, 150000, 150000,  # 40-47
            150000, 150000, 150000, 150000, 150000, 150000, 150000, 150000,  # 48-55
            150000, 150000, 150000, 150000, 150000, 150000, 150000, 150000   # 56-63
        ]
        upper_bounds = [
            10, 14, 20, 28, 38, 53, 74, 102,        # 0-7
            142, 198, 276, 384, 535, 745, 1038, 1446, # 8-15
            2014, 2806, 3909, 5446, 7587, 10570, 14726, 20516,  # 16-23
            28581, 39818, 55474, 77284, 107669, 150000, 150000, 150000,  # 24-31
            150000, 150000, 150000, 150000, 150000, 150000, 150000, 150000,  # 32-39
            150000, 150000, 150000, 150000, 150000, 150000, 150000, 150000,  # 40-47
            150000, 150000, 150000, 150000, 150000, 150000, 150000, 150000,  # 48-55
            150000, 150000, 150000, 150000, 150000, 150000, 150000, 150000   # 56-63
        ]

        # Use LCG 0 for calculation with bounds checking
        bsr_index = lcg0_bsr_index
        if bsr_index < 0 or bsr_index >= len(upper_bounds):
            # Log invalid BSR index for diagnostics
            with open(self._invalid_bsr_log_path, "a") as f:
                f.write(f"[INVALID_BSR] tti={bsr_tti}, bsr_index={bsr_index}, buffer_size={buffer_size}\n")
            return False
        requested_resource_upper = upper_bounds[bsr_index]
        requested_resource_lower = lower_bounds[bsr_index]

        # === COMMENTED OUT: Original allocation calculation with pusch, because grant tbs isnot accurate ===
        overlap_resource = 0
        for i in range(1,2): # k2 = 2
            pusch_tti = (bsr_tti + i) % 10240
            pusch_record = self.ringbuffer_pusch.find_latest_by_tti(pusch_tti)
            if pusch_record and pusch_record["logcode"] != -1:
                if pusch_record["rv_index"] == 0:
                    pusch_tbs = pusch_record["tb_size"]
                    overlap_resource += pusch_tbs
        
        if overlap_resource < requested_resource_lower:
            requested_resource_upper -= overlap_resource
            requested_resource_lower -= overlap_resource
        
        nof_grant_subframe = 0 # Find next BSR to determine allocation window
        for i in range(1,100):
            tti_value = (bsr_tti + i) % 10240
            next_bsr_record = self.ringbuffer_bsr.find_latest_by_tti(tti_value)
            next_logcode = next_bsr_record["logcode"]
            if next_logcode != -1:
                # lcg = next_bsr_record["lcg"]
                # next_bsr_index = next_bsr_record["buffer_size"][lcg]
                next_bsr_index = next_bsr_record["buffer_size"][0]
                if next_bsr_index != -1:
                    nof_grant_subframe = i
                    break
        
        allocated_resource = 0
        for j in range(1,nof_grant_subframe+1):
            grant_tti = (bsr_tti + j) % 10240
            pusch_tti = (grant_tti + 2) % 10240
            pusch_record = self.ringbuffer_pusch.find_latest_by_tti(pusch_tti)
            if pusch_record and pusch_record["logcode"] != -1:
                if pusch_record["rv_index"] == 0:
                    pusch_tbs = pusch_record["tb_size"]
                    allocated_resource += pusch_tbs

        # Calculate cellular timestamp
        cellular_timestamp = self._calculate_cellular_timestamp(bsr_tti)

        # Calculate ratio using PUSCH-based allocation (5G NR method)
        if requested_resource_upper == 0:
            if allocated_resource == 0:
                ratio = 1.0
            else:
                ratio = 2.0
        else:
            ratio = allocated_resource / requested_resource_upper * 1.0

        # Cap ratio at 2.0
        if ratio > 2.0:
            ratio = 2.0

        # Get averaged ratio for consecutive BSRs
        ratio_new, should_send = self.ratio_calculator.get_averaged_ratio(
            bsr_tti, allocated_resource, requested_resource_upper, ratio, cellular_timestamp)

        # Log ratio calculation to verification log
        with open(self._verification_log_path, "a") as f:
            f.write(f"  RATIO_CALC: bsr_tti={bsr_tti}, bsr_index={bsr_index}, "
                   f"requested={requested_resource_upper}, allocated={allocated_resource}, "
                   f"nof_grant_sf={nof_grant_subframe}, ratio={ratio:.4f}")
            if should_send:
                f.write(f" -> SEND ratio_new={ratio_new:.4f}")
            f.write("\n" + "-"*80 + "\n")

        # Send ratio to MsQuic
        if should_send and ratio_new is not None:
            self._send_ratio_to_webrtc(ratio_new)

        return True

