# Generate DIAG command to enable specific log codes
def generate_logcode_command(logcodes):
    item_ids = [code & 0xFFF for code in logcodes]
    if not item_ids: return None
    max_id = max(item_ids)
    mask_size = (max_id + 8) // 8
    mask = bytearray(mask_size)
    for code in logcodes:
        item_id = code & 0xFFF
        byte_index = item_id // 8
        bit_index = item_id % 8
        mask[byte_index] |= (1 << bit_index)
    
    cmd_header = struct.pack('<IIII', 0x73, 3, 0x0B, max_id + 1)
    full_command = cmd_header + mask
    return HDLC.encode(full_command)

def parse_0x1d_response(response_data, parser):
    """Parse 0x1D timestamp response from initialization"""
    if not response_data: return

    potential_frames = response_data.split(b'\x7e')
    
    for frame_data in potential_frames:
        if not frame_data: continue
        
        decoded_payload = HDLC.decode(frame_data + b'\x7e')
        if decoded_payload is None: continue
        
        if not decoded_payload.startswith(b'\x98\x01\x00\x00\x01\x00\x00\x00'): continue
        
        data = decoded_payload[12:]
        if len(data) < 12: continue
        
        msg_len, logcode, timestamp = struct.unpack('<HHQ', data[:12])
        payload = data[12 : 12 + msg_len]
        
        if logcode == 0x1D:
            # print("\n=== 0x1D Timestamp Response Detected in Initialization ===")
            if len(payload) >= 8:
                device_timestamp_bytes = payload[:8]
                device_timestamp = struct.unpack('<Q', device_timestamp_bytes)[0]
                
                # print("[0x1D INIT RESPONSE] Device timestamp (raw): {}".format(device_timestamp))
                # print("[0x1D INIT RESPONSE] Device timestamp (hex): 0x{:016x}".format(device_timestamp))
                # print("[0x1D INIT RESPONSE] Payload first 8 bytes: {}".format(payload[:8].hex()))
                
                if parser:
                    readable_ts = parser.convert_timestamp(device_timestamp)
                    # print("[0x1D INIT RESPONSE] Readable timestamp: {}".format(readable_ts))
                    timestamp_comment = "# Device_Internal_Timestamp: {} (0x{:016x}) - {}".format(device_timestamp, device_timestamp, readable_ts)
                    if not parser._timestamp_logged:
                        parser._write_timestamp_header(timestamp_comment)
                        parser._timestamp_logged = True
            else:
                # print("[0x1D INIT RESPONSE] Warning: Payload too short ({} bytes)".format(len(payload)))
                return

async def send_message(writer, reader, message, parser=None):
    # print("[MESSAGE] Sending message ({} bytes)".format(len(message)))
    writer.write(message)
    await writer.drain()
    await asyncio.sleep(0.1)
    try:
        response = await reader.read(16384)
        # print("[MESSAGE] Received response ({} bytes)".format(len(response)))
        
        if parser and message == b'\x1d\x1c\x3b\x7e':
            parse_0x1d_response(response, parser)
        
        return response
    except asyncio.TimeoutError:
        # print("[MESSAGE] Receive timeout, no response")
        return None

# Check if target_tti is between prev_tti and curr_tti (with wraparound)
def forward_between(prev_tti, curr_tti, target_tti):
    if prev_tti is None or curr_tti is None or target_tti is None:
        return False
    d_prev_target = (target_tti - prev_tti) % 10240
    d_prev_curr   = (curr_tti  - prev_tti) % 10240
    return 0 < d_prev_target <= d_prev_curr

# Circular buffer for storing parsed records indexed by TTI
class RingBuffer:
    def __init__(self, capacity=1000, name=""):
        self._buf = [None] * capacity
        self._cap = capacity
        self._head = 0
        self._size = 0
        self._lock = threading.Lock()
        self.name = name
        self.latest_tti = None
        self.latest_timestamp = None

    def __len__(self):
        return self._size
    
    def push_item(self, item):
        self._buf[self._head] = item
        self._head = (self._head + 1) % self._cap
        if self._size < self._cap:
            self._size += 1

    def generate_record(self, template, ts_val):
        record = {"logcode": -1, "timestamp": ts_val}
        return record
    
    def push(self, record, tti):
        with self._lock:
            prev_tti = self.latest_tti
            prev_ts = self.latest_timestamp

            tti_gap = (tti - prev_tti) % 10240 if prev_tti is not None else 0
            if prev_tti is not None and prev_ts is not None and tti_gap > 1:
                for step in range(1, tti_gap):
                    record_tti = (prev_tti + step) % 10240
                    record_ts = prev_ts + step * 0.001
                    generated_record = self.generate_record(record, record_ts)
                    self.push_item({"tti": record_tti, "record": generated_record})
            self.push_item({"tti": tti, "record": record})
            self.latest_timestamp = record["timestamp"]
            self.latest_tti = tti
            self.updated_since_combine = True

    def find_latest_by_tti(self, target_tti):
        size, head, cap = self._size, self._head, self._cap
        buf = self._buf
        for i in range(size):
            idx = (head - 1 - i) % cap
            item = buf[idx]
            if not item: continue
            if item["tti"] == target_tti:
                return item["record"]
        return None

    def find_bsr_in_range(self, start_tti, end_tti):
        # print(f"[RINGBUFFER] Search BSR between {start_tti} and {end_tti}")
        result = []
        size, head, cap = self._size, self._head, self._cap
        buf = self._buf
        for i in range(size):
            idx = (head - 1 - i) % cap
            item = buf[idx]
            if not item: continue
            tti_i = item["tti"]
            record_i = item["record"]
            logcode = record_i["logcode"]
            if logcode == -1: continue

            # lcg_i = record_i["lcg"]
            # bsr_index = record_i["buffer_size"][lcg_i]
            bsr_index = record_i["buffer_size"][0]
            if bsr_index == -1: continue

            d_start_end = (end_tti - start_tti) % 10240
            d_start_cur = (tti_i - start_tti) % 10240
            if 0 < d_start_cur < d_start_end:
                result.append((tti_i, record_i))

        # return result
        return sorted(result, key=lambda x: (x[0] - start_tti) % 10240)

# Ratio calculator for MsQuic congestion control
class ConsistentRatioCalculator:
    def __init__(self, rb_group=None):
        # For zero-request averaging between non-zero requests
        self.first_nonzero_bsr = None  # (tti, allocated, requested, ratio) - first non-zero BSR in current group
        self.group_buffer = []  # [(tti, allocated, requested, ratio, cellular_timestamp)] - all BSRs in current group
        self.pending_writes = []  # Buffered writes to be flushed
        self.ratio_file_path = "/home/qwu26/msquic_cellular/tools/cellninjia/test0/ratio_data.txt"
        self.rb_group = rb_group  # Reference to RingBufferGroup for sending ratio to MsQuic

    def get_averaged_ratio(self, tti, allocated, requested, individual_ratio, cellular_timestamp):
        """
        Get the ratio for a BSR.

        Returns: (ratio, should_send_to_msquic)
        - ratio: capped at 2.0
        - should_send: True only when both requested > 0 and allocated > 0
        """
        # Check if either requested or allocated is 0
        # If either is 0, write to file but don't send to MsQuic
        if requested == 0 or allocated == 0:
            # Write to file
            with open(self.ratio_file_path, "a") as f:
                f.write(f"{tti}\t{allocated}\t{requested}\t{individual_ratio:.6f}\n")

            # Don't send to MsQuic when requested or allocated is 0
            return (None, False)

        # Write to file
        with open(self.ratio_file_path, "a") as f:
            f.write(f"{tti}\t{allocated}\t{requested}\t{individual_ratio:.6f}\n")

        # Send to MsQuic with 2.0 cap on ratio
        capped_ratio = min(individual_ratio, 2.0)
        return (capped_ratio, True)

    def _flush_and_send_group(self):
        """Write all buffered entries with averaged ratio to file AND send to MsQuic"""
        if len(self.group_buffer) == 0:
            return

        # Calculate average ratio
        total_ratio = sum(r for _, _, _, r, _ in self.group_buffer)
        avg_ratio = total_ratio / len(self.group_buffer)

        if avg_ratio > 2.0:
            avg_ratio = 2.0

        # Write to file AND send to MsQuic (each buffered BSR gets the same averaged ratio)
        with open(self.ratio_file_path, "a") as f:
            prev_tti = None
            for tti, allocated, requested, _, cellular_timestamp in self.group_buffer:
                # Write to file
                f.write(f"{tti}\t{allocated}\t{requested}\t{avg_ratio:.6f}\n")

                if prev_tti is not None:
                    # Send to MsQuic
                    if self.rb_group:
                        self.rb_group._send_ratio_to_webrtc(avg_ratio)

                prev_tti = tti

        # Clear buffer
        self.group_buffer = []
        self.first_nonzero_bsr = None

    def finalize(self):
        """Call this at the end to flush any remaining buffered group"""
        if len(self.group_buffer) > 0:
            self._flush_and_send_group()

