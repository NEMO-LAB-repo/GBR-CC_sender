class ConsistentRatioCalculator:
    def __init__(self, rb_group=None):
        # For zero-request averaging between non-zero requests
        self.first_nonzero_bsr = None  # (tti, allocated, requested, ratio) - first non-zero BSR in current group
        self.group_buffer = []  # [(tti, allocated, requested, ratio, cellular_timestamp)] - all BSRs in current group
        self.pending_writes = []  # Buffered writes to be flushed
        self.ratio_file_path = "/home/qwu26/msquic_cellular/tools/cellninjia/test0/ratio_data.txt"
        self.rb_group = rb_group  # Reference to RingBufferGroup for sending ratio to MsQuic

    def get_averaged_ratio(self, tti, allocated, requested, individual_ratio, cellular_timestamp):
        """
        Get the ratio for a BSR.

        Returns: (ratio, should_send_to_msquic)
        - ratio: capped at 2.0
        - should_send: True only when both requested > 0 and allocated > 0
        """
        # Check if either requested or allocated is 0
        # If either is 0, write to file but don't send to MsQuic
        if requested == 0 or allocated == 0:
            # Write to file
            with open(self.ratio_file_path, "a") as f:
                f.write(f"{tti}\t{allocated}\t{requested}\t{individual_ratio:.6f}\n")

            # Don't send to MsQuic when requested or allocated is 0
            return (None, False)

        # Write to file
        with open(self.ratio_file_path, "a") as f:
            f.write(f"{tti}\t{allocated}\t{requested}\t{individual_ratio:.6f}\n")

        # Send to MsQuic with 2.0 cap on ratio
        capped_ratio = min(individual_ratio, 2.0)
        return (capped_ratio, True)

    def _flush_and_send_group(self):
        """Write all buffered entries with averaged ratio to file AND send to MsQuic"""
        if len(self.group_buffer) == 0:
            return

        # Calculate average ratio
        total_ratio = sum(r for _, _, _, r, _ in self.group_buffer)
        avg_ratio = total_ratio / len(self.group_buffer)

        if avg_ratio > 2.0:
            avg_ratio = 2.0

        # Write to file AND send to MsQuic (each buffered BSR gets the same averaged ratio)
        with open(self.ratio_file_path, "a") as f:
            prev_tti = None
            for tti, allocated, requested, _, cellular_timestamp in self.group_buffer:
                # Write to file
                f.write(f"{tti}\t{allocated}\t{requested}\t{avg_ratio:.6f}\n")

                if prev_tti is not None:
                    # Send to MsQuic
                    if self.rb_group:
                        self.rb_group._send_ratio_to_webrtc(avg_ratio)

                prev_tti = tti

        # Clear buffer
        self.group_buffer = []
        self.first_nonzero_bsr = None

    def finalize(self):
        """Call this at the end to flush any remaining buffered group"""
        if len(self.group_buffer) > 0:
            self._flush_and_send_group()

